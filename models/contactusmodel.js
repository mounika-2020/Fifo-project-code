const mongoose = require('mongoose');

const contactusschema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    phone: {
        type: Number,
        required: true,
    },
    country: {
        type: String,
        required: true,
    },
    subject: {
        type: String,
        required: true,
    },
    message: {
        type: String,
        required: true,
    },
    verified : {
        type : Boolean
    }
});
//
module.exports = new mongoose.model('contactus-data', contactusschema);
